# -------- Kontrola toka - Nested if ------

a = 4

# if...else iskaz moze biti unutar drugog if...else iskaza
if a >= 3:
    if a == 3: # Uvlacenje teksta (indentation)
        print("a je jednako 3")
    else: 
        print('a je vece od 3')
else:
    print("a je manje od 3")

