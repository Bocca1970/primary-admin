# Kvadrat binoma jednak je zbiru kvadrata oba clana i njihovog dvostrukog proizvoda

a = float(input("Unesi vrednost a: "))
b = float(input("Unesi vrednost b: "))

pkvadBinoma = (a**2 + 2*a*b + b**2)
print("(a + b) ** 2 = ", pkvadBinoma)