
# ------- Python Numbers --------

print(type(44)) # integer - svaki celi broj, bilo negativan bilo pozitivan, ukljucujuci 0
# class 'int'

print(type('12.5')) # sve izmedju navodnika, cak i broj, postaje string
# class 'str'

print(type(12.5)) # float (pun naziv - floating point) - svaki realni broj, bilo pozitivan bilo negativan
# class 'float'

print(type(2+1j)) # complex - svaki kompleksni broj
# class 'complex' 

