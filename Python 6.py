# ---------- Operatori Poredjenja ----------

n = int(input('Unesi vrednost n: '))
m = int(input('Unesi vrednost m: '))

# n > m:
print(n, '>', m, '=', n > m) # True - ako je n vece od m, inace - False

# n >= m:
print(n, '>=', m, '=', n >= m) # True - ako je n vece ili jednako m, inace - False

# n < m:
print(n, '<', m, '=', n < m) # True - ako je n manje od m, inace - False

# n <= m:
print(n, '<=', m, '=', n <= m) # True - ako je n manje ili jednako m, inace - False

# n == m:
print(n, '==', m, '=', n == m) # True - ako je n jednako m, inace - False

# n != m:
print(n, '!=', m, '=', n != m) # True - ako je n razlicito od m, inace - False
