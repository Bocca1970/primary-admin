# ------------ Operatori dodele ------------

n = int(input('Unesi vrednost n: '))

# x = n
print('x =', n) # x je ekvivalentno x = n

# x += n
print('x =', n + n) # x je ekvivalentno x = x + n

# x -= n
print('x =', n - n) # x je ekvivalentno x = x - n

# x *= n
print('x =', n * n) # x je ekvivalentno x = x * n

# x /= n
print('x =', n / n) # x je ekvivalentno x = x / n

