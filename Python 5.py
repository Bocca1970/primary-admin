# ---------- Osnovi Aritmetike -----------

n = int(input('Unesi vrednost n: '))
m = int(input('Unesi vrednost m: '))

# Cetiri osnovne aritmeticke operacije:

# Sabiranje:
print(n, '+', m, '=', n + m)

# Oduzimanje:
print(n, '-', m, '=', n - m)

# Mnozenje:
print(n, '*', m, '=', n * m)

# Deljenje:
print(n, '/', m, '=', n / m)
