
# ------- Python Conversion I--------

f = int(43.54)
print(type(f)) # Konverzija realnog broja u celi broj
# class 'int'

i = float(122)
print(type(i)) # Konverzija celog broja u realni broj
# class 'float'

n = str(44)
print(type(n)) # Konverzija celog broja u string
# class 'str'

s = int(88)
print(type(s)) # Konverzija stringa u celi broj
# class 'int'

m = str(22.123)
print(type(m)) # Konverzija realnog broja u string
# class 'str'

t = float('124.36')
print(type(t)) # Konverzija stringa u realni broj
# class 'float'